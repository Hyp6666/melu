export type TranslationKey = keyof typeof MESSAGES.en;

export function resolveUiLanguage(language: UiLanguage | null | undefined): UiLanguage {
  return language ?? "en";
}

export function getUiLanguageLabel(language: UiLanguage): string {
  return SUPPORTED_UI_LANGUAGES.find((item) => item.value === language)?.label ?? "English";
}

export function t(
  language: UiLanguage | null | undefined,
  key: TranslationKey,
  params: TranslationParams = {},
): string {
  const resolved = resolveUiLanguage(language);
  let template: string = MESSAGES[resolved][key] ?? MESSAGES.en[key];
  for (const [name, value] of Object.entries(params)) {
    template = template.replaceAll(`{${name}}`, String(value));
  }
  return template;
}

export function createI18n(language: UiLanguage | null | undefined) {
  const resolved = resolveUiLanguage(language);
  return {
    language: resolved,
    t(key: TranslationKey, params: TranslationParams = {}): string {
      return t(resolved, key, params);
    },
  };
}

export function getLocalizedMirrorLabel(
  mirror: MirrorName,
  _language: UiLanguage | null | undefined,
): string {
  if (mirror === "huggingface") {
    return "Hugging Face (Global)";
  }
  return "ModelScope (CN)";
}
